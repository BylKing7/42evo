#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*s1;

	i = 0;
	while (s[i])
		i++;
	s1 = (char *)calloc(i, sizeof(s));
	i = 0;
	while (s[i])
	{
		s1[i] = s[i];
		i++;
	}
	return (s1);
}
